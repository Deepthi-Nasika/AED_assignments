    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblPerson.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        Person selectedPerson = (Person) model.getValueAt(selectedRowIndex, 0);
        
        txtFirstName.setText(String.valueOf(selectedPerson.getFirst_name()));
        txtLastName.setText(String.valueOf(selectedPerson.getLast_name()));
        txtGender.setText(String.valueOf(selectedPerson.getGender()));
        txtDOB.setText(String.valueOf(selectedPerson.getDob()));
        txtAge.setText(String.valueOf(selectedPerson.getAge()));
        txtEmailID.setText(String.valueOf(selectedPerson.getEmail_id()));
        
        
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblPerson.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        Person selectedPerson = (Person) model.getValueAt(selectedRowIndex, 0);
        
        person_history.deletePerson(selectedPerson);
        
        JOptionPane.showMessageDialog(this, "Person deleted");
        
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed


