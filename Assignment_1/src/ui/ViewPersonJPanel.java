    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        model.setRowCount(0);
        
        for (Person p : person_history.getPerson_history()){
            
            Object[] row = new Object[3];
            row[0] = p;
            row[1] = p.getLast_name();
            row[2] = p.getDob();
            
            model.addRow(row);
            
        }
        
        
    }
