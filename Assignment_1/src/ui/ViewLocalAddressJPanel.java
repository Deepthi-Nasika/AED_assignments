    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblLocalAddress.getModel();
        model.setRowCount(0);
        
        for(Local_Address lcl : local_adr_history.getLocal_adr_history()){
            
            Object[] row = new Object[4];
            row[0] = lcl;
            row[1] = lcl.getState();
            row[2] = lcl.getCountry();
            row[3] = lcl.getPostalcode();
            
            model.addRow(row);
        }
        
    }
