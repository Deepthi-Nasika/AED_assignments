    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblLocalAddress.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblLocalAddress.getModel();
        Local_Address selectedLocalAddress = (Local_Address) model.getValueAt(selectedRowIndex, 0);
        
        local_adr_history.deleteLocalAddress(selectedLocalAddress);
        
        JOptionPane.showMessageDialog(this, "Local Address deleted");
        
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblLocalAddress.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblLocalAddress.getModel();
        Local_Address selectedLocalAddress = (Local_Address) model.getValueAt(selectedRowIndex, 0);
        
        txtName.setText(String.valueOf(selectedLocalAddress.getName()));
        txtStreet.setText(String.valueOf(selectedLocalAddress.getStreet()));
        txtCity.setText(String.valueOf(selectedLocalAddress.getCity()));
        txtState.setText(String.valueOf(selectedLocalAddress.getState()));
        txtCountry.setText(String.valueOf(selectedLocalAddress.getCountry()));
        txtPostalCode.setText(String.valueOf(selectedLocalAddress.getPostalcode()));
        
        
    }//GEN-LAST:event_btnViewActionPerformed


