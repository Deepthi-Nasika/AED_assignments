    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblHomeAddress.getModel();
        model.setRowCount(0);
        
        for (Home_Address hm : home_adr_history.getHome_adr_history()){
            
            Object[] row = new Object[3];
            row[0] = hm;
            row[1] = hm.getState();
            row[2] = hm.getZipcode();
            
            model.addRow(row);
            
        }
        
    }
