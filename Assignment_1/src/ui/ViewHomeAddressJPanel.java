    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblHomeAddress.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblHomeAddress.getModel();
        Home_Address selectedHomeAddress = (Home_Address) model.getValueAt(selectedRowIndex, 0);
        
        home_adr_history.deleteHomeAddress(selectedHomeAddress);
        
        JOptionPane.showMessageDialog(this, "Home Address deleted");
        
        populateTable();
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblHomeAddress.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblHomeAddress.getModel();
        Home_Address selectedHomeAddress = (Home_Address) model.getValueAt(selectedRowIndex, 0);
        
        txtName.setText(String.valueOf(selectedHomeAddress.getName()));
        txtAdr1.setText(String.valueOf(selectedHomeAddress.getAdr_1()));
        txtAdr2.setText(String.valueOf(selectedHomeAddress.getAdr_2()));
        txtCity.setText(String.valueOf(selectedHomeAddress.getCity()));
        txtState.setText(String.valueOf(selectedHomeAddress.getState()));
        txtZipCode.setText(String.valueOf(selectedHomeAddress.getZipcode()));
    
        
    }//GEN-LAST:event_btnViewActionPerformed


