    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblBankAccount.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblBankAccount.getModel();
        Bank_Account selectedBankAccount = (Bank_Account) model.getValueAt(selectedRowIndex, 0);
        
        bank_acc_history.deleteBankAccount(selectedBankAccount);
        
        JOptionPane.showMessageDialog(this, "Bank Account deleted");
        
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblBankAccount.getSelectedRow();
        
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblBankAccount.getModel();
        Bank_Account selectedBankAccount = (Bank_Account) model.getValueAt(selectedRowIndex, 0);
        
        txtName.setText(String.valueOf(selectedBankAccount.getName()));
        txtBankName.setText(String.valueOf(selectedBankAccount.getBank_name()));
        txtActType.setText(String.valueOf(selectedBankAccount.getAcc_type()));
        txtActNum.setText(String.valueOf(selectedBankAccount.getAcc_no()));
        txtDbtAmt.setText(String.valueOf(selectedBankAccount.getDebit_amt()));
        txtCrdtAmt.setText(String.valueOf(selectedBankAccount.getCredit_amt()));
        
    }//GEN-LAST:event_btnViewActionPerformed


