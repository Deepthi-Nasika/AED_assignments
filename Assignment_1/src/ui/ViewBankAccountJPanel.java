    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblBankAccount.getModel();
        model.setRowCount(0);
        
        for(Bank_Account bnk : bank_acc_history.getBank_acc_history()){
            
            Object[] row = new Object[4];
            row[0] = bnk;
            row[1] = bnk.getBank_name();
            row[2] = bnk.getAcc_no();
            
            model.addRow(row);
        }
    }
